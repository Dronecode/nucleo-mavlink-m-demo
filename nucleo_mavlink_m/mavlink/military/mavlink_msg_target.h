/**
 * @brief Pack a target message
 * @param system_id ID of this system
 * @param component_id ID of this component (e.g. 200 for IMU)
 * @param msg The MAVLink message to compress the data into
 *
 * @param time_usec [us] Timestamp (UNIX epoch, microseconds UTC).
 * @param target_time_usec [us] Timestamp when the target was detected or designated (UNIX epoch, microseconds UTC).
 * @param target_id  Unique target identifier generated by the source system.
 * @param target_set_id  Parent target set identifier.
 * @param package_id_hash  Compact hash or identifier for the associated target package. 0 if no package is associated.
 * @param target_name  Human-readable target name (null-terminated).
 * @param lat [degE7] Target latitude, WGS84.
 * @param lon [degE7] Target longitude, WGS84.
 * @param alt [m] Target altitude, MSL.
 * @param vx [m/s] Velocity north component (NED).
 * @param vy [m/s] Velocity east component (NED).
 * @param vz [m/s] Velocity down component (NED).
 * @param cov_pos_x  Position covariance, north axis (m^2).
 * @param cov_pos_y  Position covariance, east axis (m^2).
 * @param cov_pos_z  Position covariance, down axis (m^2).
 * @param cov_vel_x  Velocity covariance, north axis (m^2/s^2).
 * @param cov_vel_y  Velocity covariance, east axis (m^2/s^2).
 * @param cov_vel_z  Velocity covariance, down axis (m^2/s^2).
 * @param cep_desired [m] Desired circular error probable.
 * @param cep_max [m] Maximum acceptable circular error probable.
 * @param flags  Target warning and status flags.
 * @param package_endpoint_ip_1  Primary IPv4 endpoint address for target package retrieval, packed as (a<<24)|(b<<16)|(c<<8)|d for address a.b.c.d; for example, 192.168.1.10 is 0xC0A8010A. 0 if unused.
 * @param package_endpoint_ip_2  Secondary IPv4 endpoint address for target package retrieval, packed as (a<<24)|(b<<16)|(c<<8)|d for address a.b.c.d. 0 if unused.
 * @param package_endpoint_ip_3  Third IPv4 endpoint address for target package retrieval, packed as (a<<24)|(b<<16)|(c<<8)|d for address a.b.c.d. 0 if unused.
 * @param target_class  Target classification.
 * @param target_domain  Primary operating domain of the target.
 * @param target_entity_2525d  Optional MIL-STD-2525D land target entity refinement. UNKNOWN if not provided or not a land entity.
 * @param target_force  Force affiliation.
 * @param confidence  Classification or detection confidence scaled from 0 to 10000, where 0 = 0.0 and 10000 = 1.0.
 * @param package_endpoint_port  TCP port for target package retrieval. 0 if no package is associated.
 * @param sensor_type  Primary sensor source used for target identification.
 * @param package_transport  Transport protocol used to retrieve the target package.
 * @param package_path  Path component for target package retrieval, null-terminated. Empty if no package is associated.
 * @param prf_code  Laser designator PRF code for SAL engagement (e.g. 1111-1788). 0 = no laser code assigned.
 * @param tle_category  Target-location-error category (label only, not a mensuration figure).
 * @param dmpi_reference_kind  How the DMPI reference is expressed. A mensurated DMPI tied to a weapon solution is NOT carried.
 * @param restricted_target_flags  No-strike / restricted-target / no-fire-area context flags (bitmask).
 * @return length of the message in bytes (excluding serial stream start sign)
 */
static inline uint16_t mavlink_msg_target_pack(uint8_t system_id, uint8_t component_id, mavlink_message_t* msg,
                               uint64_t time_usec, uint64_t target_time_usec, uint32_t target_id, uint32_t target_set_id, uint32_t package_id_hash, const char *target_name, int32_t lat, int32_t lon, float alt, float vx, float vy, float vz, float cov_pos_x, float cov_pos_y, float cov_pos_z, float cov_vel_x, float cov_vel_y, float cov_vel_z, float cep_desired, float cep_max, uint32_t flags, uint32_t package_endpoint_ip_1, uint32_t package_endpoint_ip_2, uint32_t package_endpoint_ip_3, uint8_t target_class, uint8_t target_domain, uint8_t target_entity_2525d, uint8_t target_force, uint16_t confidence, uint16_t package_endpoint_port, uint8_t sensor_type, uint8_t package_transport, const char *package_path, uint16_t prf_code, uint8_t tle_category, uint8_t dmpi_reference_kind, uint8_t restricted_target_flags)
{
#if MAVLINK_NEED_BYTE_SWAP || !MAVLINK_ALIGNED_FIELDS
    char buf[MAVLINK_MSG_ID_TARGET_LEN];
    _mav_put_uint64_t(buf, 0, time_usec);
    _mav_put_uint64_t(buf, 8, target_time_usec);
    _mav_put_uint32_t(buf, 16, target_id);
    _mav_put_uint32_t(buf, 20, target_set_id);
    _mav_put_uint32_t(buf, 24, package_id_hash);
    _mav_put_int32_t(buf, 28, lat);
    _mav_put_int32_t(buf, 32, lon);
    _mav_put_float(buf, 36, alt);
    _mav_put_float(buf, 40, vx);
    _mav_put_float(buf, 44, vy);
    _mav_put_float(buf, 48, vz);
    _mav_put_float(buf, 52, cov_pos_x);
    _mav_put_float(buf, 56, cov_pos_y);
    _mav_put_float(buf, 60, cov_pos_z);
    _mav_put_float(buf, 64, cov_vel_x);
    _mav_put_float(buf, 68, cov_vel_y);
    _mav_put_float(buf, 72, cov_vel_z);
    _mav_put_float(buf, 76, cep_desired);
    _mav_put_float(buf, 80, cep_max);
    _mav_put_uint32_t(buf, 84, flags);
    _mav_put_uint32_t(buf, 88, package_endpoint_ip_1);
    _mav_put_uint32_t(buf, 92, package_endpoint_ip_2);
    _mav_put_uint32_t(buf, 96, package_endpoint_ip_3);
    _mav_put_uint16_t(buf, 100, confidence);
    _mav_put_uint16_t(buf, 102, package_endpoint_port);
    _mav_put_uint16_t(buf, 104, prf_code);
    _mav_put_uint8_t(buf, 156, target_class);
    _mav_put_uint8_t(buf, 157, target_domain);
    _mav_put_uint8_t(buf, 158, target_entity_2525d);
    _mav_put_uint8_t(buf, 159, target_force);
    _mav_put_uint8_t(buf, 160, sensor_type);
    _mav_put_uint8_t(buf, 161, package_transport);
    _mav_put_uint8_t(buf, 242, tle_category);
    _mav_put_uint8_t(buf, 243, dmpi_reference_kind);
    _mav_put_uint8_t(buf, 244, restricted_target_flags);
    _mav_put_char_array(buf, 106, target_name, 50);
    _mav_put_char_array(buf, 162, package_path, 80);
        memcpy(_MAV_PAYLOAD_NON_CONST(msg), buf, MAVLINK_MSG_ID_TARGET_LEN);
#else
    mavlink_target_t packet;
    packet.time_usec = time_usec;
    packet.target_time_usec = target_time_usec;
    packet.target_id = target_id;
    packet.target_set_id = target_set_id;
    packet.package_id_hash = package_id_hash;
    packet.lat = lat;
    packet.lon = lon;
    packet.alt = alt;
    packet.vx = vx;
    packet.vy = vy;
    packet.vz = vz;
    packet.cov_pos_x = cov_pos_x;
    packet.cov_pos_y = cov_pos_y;
    packet.cov_pos_z = cov_pos_z;
    packet.cov_vel_x = cov_vel_x;
    packet.cov_vel_y = cov_vel_y;
    packet.cov_vel_z = cov_vel_z;
    packet.cep_desired = cep_desired;
    packet.cep_max = cep_max;
    packet.flags = flags;
    packet.package_endpoint_ip_1 = package_endpoint_ip_1;
    packet.package_endpoint_ip_2 = package_endpoint_ip_2;
    packet.package_endpoint_ip_3 = package_endpoint_ip_3;
    packet.confidence = confidence;
    packet.package_endpoint_port = package_endpoint_port;
    packet.prf_code = prf_code;
    packet.target_class = target_class;
    packet.target_domain = target_domain;
    packet.target_entity_2525d = target_entity_2525d;
    packet.target_force = target_force;
    packet.sensor_type = sensor_type;
    packet.package_transport = package_transport;
    packet.tle_category = tle_category;
    packet.dmpi_reference_kind = dmpi_reference_kind;
    packet.restricted_target_flags = restricted_target_flags;
    mav_array_memcpy(packet.target_name, target_name, sizeof(char)*50);
    mav_array_memcpy(packet.package_path, package_path, sizeof(char)*80);
        memcpy(_MAV_PAYLOAD_NON_CONST(msg), &packet, MAVLINK_MSG_ID_TARGET_LEN);
#endif

    msg->msgid = MAVLINK_MSG_ID_TARGET;
    return mavlink_finalize_message(msg, system_id, component_id, MAVLINK_MSG_ID_TARGET_MIN_LEN, MAVLINK_MSG_ID_TARGET_LEN, MAVLINK_MSG_ID_TARGET_CRC);
}

